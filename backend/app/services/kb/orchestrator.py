"""Document Orchestrator — parses any KB document into typed blocks.

Runs at ingest (and on every edit per the locked decision: full re-parse
on every edit/sync — drift between source doc and structured chunks is
worse than the LLM cost). Each typed block becomes a ``KBChunk`` row
with ``kind`` set, ``extracted_metadata`` populated per the per-kind
schema, and an embedding generated by the existing Voyage embedder.

The orchestrator is *additive* on top of the existing chunker: if the
LLM call fails or produces no usable blocks, we fall back to token-based
chunks tagged ``kind='context'`` so retrieval still has something to
work with. This keeps KB ingest robust to LLM hiccups.

Side effects beyond writing chunks:

* Populates ``kb_integration_gaps`` for procedures that reference
  integrations the tenant has not connected.
* On admin connect/disconnect of an integration, ``reevaluate_gaps``
  refreshes the gap table without re-running the orchestrator.
"""
